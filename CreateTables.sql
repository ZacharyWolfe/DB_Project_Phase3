-- DINE_IN TABLE
CREATE TABLE DINE_IN (
  -- Keys of DINE_IN
  PRIMARY KEY (ID)
  FOREIGN KEY
    (ID) REFERENCES ORDER(ORDER_ID),
  
  -- Attributes of DINE_IN
  ID INT,
  TABLE_NUMBER INT,
);

-- DELIVERY TABLE
CREATE TABLE DELIVERY (
  -- Keys of DELIVERY
  PRIMARY KEY (ID)
  FOREIGN KEY
    (ID) REFERENCES ORDER(ORDER_ID),

  -- Attributes of DELIVERY
  ID INT,
);

-- PICKUP TABLE
CREATE TABLE PICKUP (
  -- Keys of PICKUP
  PRIMARY KEY (ID)
  FOREIGN KEY
    (ID) REFERENCES ORDER(ORDER_ID),

  -- Attributes of PICKUP
  ID INT,
);



CREATE TABLE SEATS (
  -- Keys of SEATS
  PRIMARY KEY (SEAT_ID),
  FOREIGN KEY
    (SEAT_ID) REFERENCES DINE_IN(ID),
  
  -- Attributes of SEATS
  SEAT_ID INT,
  SEAT_NUMBER INT,
);

CREATE TABLE CUSTOMER (
  -- Keys of CUSTOMER
  PRIMARY KEY (CUSTOMER_ID),
  
  -- Attributes of CUSTOMER
  CUSTOMER_ID INT,
  PHONE_NUMBER VARCHAR(10),
  ADDRESS VARCHAR(100),
  NAME VARCHAR(100),
);

CREATE TABLE ORDER_DISCOUNT (
  -- Keys of ORDER_DISCOUNT
  PRIMARY KEY (ID),
  FOREIGN KEY 
    (DISCOUNT_ID) REFERENCES DISCOUNT(DISCOUNT_ID),
  
  -- Attributes of ORDER_DISCOUNT
  ID INT,
  DISCOUNT_ID INT,
);

CREATE TABLE DISCOUNT (
  -- Keys of DISCOUNT
  PRIMARY KEY (DISCOUNT_ID),

  -- Attributes of DISCOUNT
  DISCOUNT_ID INT,
  NAME VARCHAR(50),
  PERCENT_OFF(3, 2),
  DOLLAR_OFF(10, 2),
);


