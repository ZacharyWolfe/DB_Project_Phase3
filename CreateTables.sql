-- ORDER TABLE

CREATE TABLE ORDER (
  -- Keys of ORDER
  PRIMARY KEY (ORDER_ID),
  FOREIGN KEY 
    (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID),

  -- Attributes of ORDER
  ORDER_ID INT,
  TOTAL_PRICE DECIMAL(10, 2),
  BUSINESS_COST DECIMAL(10, 2),
);

