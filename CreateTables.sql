CREATE TABLE PIZZA (
  -- Keys of PIZZA
  PRIMARY KEY (PIZZA_ID),
  FOREIGN KEY
    (ORDER_ID) REFERENCES ORDER(ORDER_ID),

  -- Attributes of PIZZA
  PIZZA_ID INT,
  CRUST_TYPE VARCHAR(50),
  PRICE DECIMAL(10, 2),
  SIZE VARCHAR(25),
  BUSINESS_COST DECIMAL(10,2),
  STATUS VARCHAR(25),
  TIME_STAMP TIMESTAMP,
);

CREATE TABLE BASE_PRICE (
  -- Keys of BASE_PRICE
  PRIMARY KEY (CRUST_TYPE),
  FOREIGN KEY
    (SIZE) REFERENCES PIZZA(SIZE),
  
  -- Attributes of BASE_PRICE
  CRUST_TYPE VARCHAR(50),
  SIZE VARCHAR(25),
  PRICE DECIMAL(10, 2),
  BASE_COST DECIMAL(10, 2),
);

CREATE TABLE TOPPING (
  -- Keys of TOPPING
  PRIMARY KEY (NAME),

  -- Attributes of TOPPING
  NAME VARCHAR(50),
  BUSINESS_COST DECIMAL(10, 2),
  AMOUNT DECIMAL(5, 2),
  PRICE DECIMAL(10, 2),
  INVENTORY_LEVEL INT,
);

CREATE TABLE HAS_TOPPING (
  PRIMARY KEY (NAME),
  FOREIGN KEY
    (PIZZA_ID) REFERENCES PIZZA(PIZZA_ID),
  
  -- Attributes of HAS_TOPPING
  NAME VARCHAR(50),
  PIZZA_ID INT,
  REQUIRES_DOUBLE BOOLEAN,
);